_組到母表 = [
    ['幫', '幫滂並明'],
    ['端', '端透定泥'],
    ['知', '知徹澄孃'],
    ['精', '精清從心邪'],
    ['莊', '莊初崇生俟'],
    ['章', '章昌船書常'],
    ['見', '見溪羣疑'],
    ['影', '影曉匣云'],
    [None, '來日以'],
]

_d母到組 = {母: 組 for 組, 母們 in _組到母表 for 母 in 母們}


def 母到組(母: str):
    '''
    將母轉換爲組。

    ```python
    >>> 母到組('滂')
    '幫'
    >>> 母到組('云')
    '影'
    >>> 母到組('以')
    None
    ```
    '''
    return _d母到組[母]
